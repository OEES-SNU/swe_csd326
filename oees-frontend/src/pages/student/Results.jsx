import { useState, useEffect } from 'react'
import { useLocation } from 'react-router-dom'
import api from '../../api/axios'

export default function Results() {
  const { state } = useLocation()
  const [exams, setExams] = useState([])
  const [selectedExam, setSelectedExam] = useState('')
  const [result, setResult] = useState(null)
  const [loading, setLoading] = useState(false)
  const [error, setError] = useState('')

  useEffect(() => {
    api.get('/exams/available')
      .then((r) => {
        setExams(r.data)
        if (r.data.length > 0) setSelectedExam(String(r.data[0].id))
      })
  }, [])

  useEffect(() => {
    if (!selectedExam) return
    setLoading(true)
    setResult(null)
    setError('')
    api.get(`/results/exam/${selectedExam}/my`)
      .then((r) => setResult(r.data))
      .catch((err) => {
        if (err.response?.status === 404) setError('No result found yet. Results may not have been generated.')
        else setError('Failed to load result.')
      })
      .finally(() => setLoading(false))
  }, [selectedExam])

  return (
    <>
      <div className="page-header">
        <div>
          <h1 className="page-title">My Results</h1>
          <p className="page-subtitle">View your exam scores and grades</p>
        </div>
      </div>

      {state?.submitted && (
        <div className="mb-5 px-4 py-3 bg-green-50 border border-green-200 rounded text-sm text-green-700">
          Exam submitted successfully. Results will be available once generated by your instructor.
        </div>
      )}

      <div className="mb-5">
        <label className="label">Select exam</label>
        <select className="select max-w-sm" value={selectedExam}
          onChange={(e) => setSelectedExam(e.target.value)}>
          {exams.map((ex) => (
            <option key={ex.id} value={ex.id}>{ex.title} — {ex.courseName}</option>
          ))}
        </select>
      </div>

      {loading && <div className="text-sm text-gray-400 py-10 text-center">Loading result…</div>}

      {error && !loading && (
        <div className="card p-8 text-center">
          <p className="text-gray-400 text-sm">{error}</p>
        </div>
      )}

      {result && !loading && (
        <div className="max-w-lg space-y-4">
          {/* Score card */}
          <div className="card p-6">
            <div className="flex items-start justify-between">
              <div>
                <h2 className="text-lg font-semibold text-gray-900">{result.examTitle}</h2>
                <p className="text-sm text-gray-400 mt-0.5">Exam result</p>
              </div>
              <div className="text-right">
                <div className={`text-3xl font-bold ${result.passed ? 'text-green-600' : 'text-red-600'}`}>
                  {result.totalMarks}
                  <span className="text-base font-normal text-gray-400">/{result.maxMarks}</span>
                </div>
                <div className={`text-sm font-medium mt-0.5 ${result.passed ? 'text-green-600' : 'text-red-600'}`}>
                  {result.passed ? 'Passed' : 'Failed'}
                </div>
              </div>
            </div>

            <div className="mt-4 pt-4 border-t border-gray-100 grid grid-cols-3 gap-4">
              <div className="text-center">
                <p className="text-xs text-gray-400 mb-1">Grade</p>
                <p className="text-xl font-bold text-gray-900">{result.grade ?? '—'}</p>
              </div>
              <div className="text-center">
                <p className="text-xs text-gray-400 mb-1">Rank</p>
                <p className="text-xl font-bold text-gray-900">
                  {result.rank != null ? `#${result.rank}` : '—'}
                </p>
              </div>
              <div className="text-center">
                <p className="text-xs text-gray-400 mb-1">Class avg</p>
                <p className="text-xl font-bold text-gray-900">
                  {result.examAverage != null ? result.examAverage.toFixed(1) : '—'}
                </p>
              </div>
            </div>
          </div>

          {/* Score bar */}
          <div className="card p-4">
            <div className="flex items-center justify-between text-xs text-gray-400 mb-2">
              <span>Score</span>
              <span>{((result.totalMarks / result.maxMarks) * 100).toFixed(1)}%</span>
            </div>
            <div className="w-full bg-gray-100 rounded-full h-2">
              <div
                className={`h-2 rounded-full ${result.passed ? 'bg-green-500' : 'bg-red-500'}`}
                style={{ width: `${Math.min(100, (result.totalMarks / result.maxMarks) * 100)}%` }}
              />
            </div>
          </div>
        </div>
      )}
    </>
  )
}
